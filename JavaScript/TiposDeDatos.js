// Tipos de datos
// Numerico -> number
// Numerico -> BigInt
console.log(12345);
console.log(5.16513213512345);

// Texto -> string
console.log("Daniel Salamanca");

// Tipo Lógico -> Boolean
console.log(true);
console.log(false);

// Nulo -> null
console.log(null);

//  indefinido -> undefined
console.log(undefined);

// Objetos -> object
// tiene Llave y Valor -> key and Value
console.log({ nombre:"Daniel Salamanca", edad: 31, esHombre:true });
// Listas -> Array
// lista de textos
console.log(["Colombia","España","Brasil"]);
// Lista numerica
console.log([31,32,33,34,35,36]);
// Lista de Objetos
console.log([{ nombre:"Daniel Salamanca", edad: 31, esHombre:true },{ nombre:"Ingrid Garzon", edad: 31, esHombre:false }]);



