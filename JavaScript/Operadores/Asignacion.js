// Operadores asignación
// asignacion de valor -> =
let a = "Texto";
console.log(a);

// Asignacion y adicion -> +=
let b = 1;
console.log(b);
b += b; 
console.log(b);
console.log(b =+ b);
// Asignacion y resta -> -=
let c = 5;
c -= c - 1;
console.log(c);

//asignacion de multiplicacion
let d = 5;
d *= d;
console.log(d);

// Operador de residuo
// Modulo -> %=
let e = 34;
e %= 2;
console.log(e);

// Asignacion de exponenciación
// Elevado a  -> **=
let f = 2;
f **= 5
console.log(f);






