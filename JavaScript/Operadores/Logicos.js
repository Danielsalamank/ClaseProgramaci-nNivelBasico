// Operadores Logicos
// Tablas de verdad en matematicas
// And = y  -> &&  + + = +, - - = +, + - = -, - + = - 
// OR = o   -> ||  + - = +, - + = + , + + = +, - - = - 
// Not = no -> !

// and -> &&
console.log(33 >= 23 && 23 >= 24);

// OR -> ||
//             true        false
console.log(33 >= 23 || 23 >= 24);

// NOT = !
console.log( 5 !== 6 );
